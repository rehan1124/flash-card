const createStarterCards = (): Card[] => {
  const createdAt = new Date().toISOString()

  return starterEntries.map(([marathi, transliteration, english]) => ({
    id: createId(),
    marathi,
    english,
    transliteration,
    wrongCount: 0,
    seenCount: 0,
    createdAt,
  }))
}

export const createStarterDeck = (): Deck => {
  const now = new Date().toISOString()

  return {
    id: createId(),
    title: 'Marathi Starter Deck (100)',
    description: '100 common Marathi words and phrases for beginners.',
    createdAt: now,
    updatedAt: now,
    cards: createStarterCards(),
  }
}
